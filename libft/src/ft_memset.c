#include "../include/libft.h"

void		*ft_memset(void *s, int c, ssize_t n)
{
	ssize_t	i;
	char	*tmp;

	i = 0;
	tmp = s;
	while (i < n)
		tmp[i++] = c;
	return (s);
}
